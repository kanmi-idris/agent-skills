#!/usr/bin/env bash
set -euo pipefail

cat <<'EOF'
Lunour Naming — workflow

1) Brand discovery
- What does the company do?
- Who is the customer?
- What should the name make people feel?
- What adjectives describe the brand?
- What names do they love or hate?
- What constraints exist?

2) Candidate generation
- Generate 15–25 names
- Group by naming category
- Add rationale, domain note, and risk flag

3) Evaluation
- Evocative
- Memorable
- Spellable
- Speakable
- Ownable
- Scalable
- Domain-viable

4) Finalist development
- Brand story
- Taglines
- Domain strategy
- Social handle guidance
- Linguistic gut check
EOF
